package shzeproject.operators;

public class ExponentialNumber {
    public static void main(String[] args) {
        System.out.println("Максимальная экспонента для Float "+Float.MAX_VALUE);
        System.out.println("Минимальная экспонента для Float "+ (-Float.MAX_VALUE));

        System.out.println("Максимальная экспонента для Double "+ Double.MAX_VALUE);
        System.out.println("Минимальная экспонента для Double " +(-Double.MAX_VALUE));

    }
}
